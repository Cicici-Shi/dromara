<h2 id="项目捐赠" tabindex="-1"><a class="header-anchor" href="#项目捐赠" aria-hidden="true">#</a> 项目捐赠</h2>
<p>Dromara 开源社区组织对捐赠项目有以下最基本条件要求：</p>
<ul>
<li><strong>项目必须是原创且不能是 Fork 版本。</strong></li>
<li>项目必须是完整的应用解决方案。</li>
<li>项目必须有良好的代码注释和不断完善的使用文档。</li>
<li>项目至少在近 2 个月内有实际有效的代码提交记录。</li>
<li><strong>项目必须得到 dromara 社区组织评委会的认可。</strong></li>
<li>优先考虑 Gitee 推荐项目或已获得 GVP 的项目。（GVP 项目需联系 Gitee 官方人员取消 GVP 后才能转移到组织，转移成功后恢复 GVP）</li>
<li>优选考虑 容器工具、微服务框架和工具、分布式事务、分布式中间件、大数据处理、人工智能、IoT 物联网、开发/测试/运维相关工具链等领域项目。</li>
